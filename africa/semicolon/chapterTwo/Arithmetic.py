firstOperand = 27.5
secondOperand = 2


def add():
    return firstOperand + secondOperand


def subtract():
    return firstOperand - secondOperand


def multiply():
    return firstOperand * secondOperand


def division():
    return firstOperand / secondOperand


def floor_division():
    return firstOperand // secondOperand


def exponential():
    return firstOperand ** secondOperand


print(add(), subtract(), multiply(), division(), floor_division(), exponential())

